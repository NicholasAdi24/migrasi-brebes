                                    <h5>Apa risiko yang dapat dialami apabila saya berangkat menjadi Pekerja Migran Indonesia (PMI) melalui jalur tidak resmi?</h5>

                                    <br>
                                    Berangkat ke luar negeri sebagai PMI melalui jalur tidak resmi merupakan tindakan yang sangat berisiko, baik dari sisi hukum maupun keselamatan. Berdasarkan Undang-Undang Nomor 18 Tahun 2017 tentang Pelindungan Pekerja Migran Indonesia, dinyatakan secara tegas bahwa setiap calon PMI wajib melalui proses penempatan yang dilakukan oleh Pemerintah Pusat, Pemerintah Daerah, dan/atau Perusahaan Penempatan Pekerja Migran Indonesia (P3MI) yang memiliki izin. Pada Pasal 69, disebutkan bahwa "Setiap orang dilarang menempatkan WNI untuk bekerja di luar negeri tanpa memenuhi persyaratan sebagaimana diatur dalam undang-undang ini." Pelanggaran terhadap ketentuan tersebut dapat dikenakan sanksi pidana.
                                    Dampak yang dialami oleh PMI non-prosedural sangat serius. Mereka tidak terdaftar secara resmi di BP2MI dan tidak memiliki kontrak kerja yang sah. Akibatnya, jika terjadi pelanggaran seperti gaji tidak dibayarkan, jam kerja berlebihan, tindak kekerasan, atau pelecehan, negara tidak dapat memberikan perlindungan secara maksimal. Bahkan, banyak PMI non-prosedural yang menjadi korban perdagangan orang (human trafficking), yang secara hukum termasuk ke dalam tindak pidana berat sebagaimana diatur dalam Pasal 4 dan 10 UU No. 21 Tahun 2007 tentang Pemberantasan Tindak Pidana Perdagangan Orang.
                                    Selain itu, PMI yang berangkat secara ilegal juga sering kali mengalami hambatan saat ingin pulang ke tanah air. Karena tidak memiliki dokumen sah, mereka dapat ditahan oleh otoritas setempat, dideportasi dengan kondisi tidak layak, atau bahkan terjebak dalam proses hukum negara tujuan. Tidak jarang, keluarga di kampung halaman pun turut terdampak secara ekonomi maupun psikologis karena kehilangan kontak atau kepastian nasib anggota keluarga yang bekerja di luar negeri.

