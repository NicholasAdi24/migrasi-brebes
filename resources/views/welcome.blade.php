                                    <br><br>Jika PMI mengalami pemutusan kontrak sepihak, PMI dapat mengajukan klaim PHK melalui BPJS Ketenagakerjaan untuk mendapatkan manfaat seperti santunan sebesar Rp1,5 juta dan penggantian biaya tiket pulang maksimal Rp15 juta. Klaim ini dapat diajukan baik saat PMI masih di negara penempatan maupun setelah dipulangkan. Selanjutnya, dalam hal pemutusan kontrak, pengguna jasa (majikan) wajib mengurus dokumen administrasi kepulangan, termasuk surat pernyataan pelepasan dan pengurusan tiket pulang PMI. Jika PMI ingin pulang ke Indonesia, majikan harus menanggung biaya tiket pesawat dan transportasi darat sampai ke daerah asal PMI. Jika PMI masih ingin bekerja, agen penempatan wajib mencarikan pengguna jasa baru tanpa pemotongan gaji
                                    <br><br>Selain itu, PMI disarankan untuk melaporkan kasus pemutusan kontrak sepihak atau pemaksaan pemulangan ke perwakilan resmi Indonesia di negara tujuan, seperti Kedutaan Besar Republik Indonesia (KBRI) atau Kantor Dagang dan Ekonomi Indonesia (KDEI), serta menggunakan hotline pengaduan tenaga kerja migran agar mendapatkan mediasi dan perlindungan hukum

