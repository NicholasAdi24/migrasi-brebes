                                    <h5>Apakah Pekerja Migran Indonesia (PMI) berhak mendapatkan jaminan sosial dan asuransi selama bekerja di luar negeri?</h5>

                                    <br>
                                    Pekerja Migran Indonesia (PMI) berhak mendapatkan jaminan sosial dan asuransi selama bekerja di luar negeri, sebagaimana diatur dalam berbagai peraturan perundang-undangan di Indonesia. Hak ini merupakan bagian dari upaya negara untuk memberikan perlindungan menyeluruh kepada PMI, baik sebelum, selama, maupun setelah masa penempatan. PMI yang terdaftar secara resmi berhak atas beberapa program jaminan sosial dan asuransi, di antaranya:
                                    <br>a.	Jaminan Kecelakaan Kerja (JKK): Memberikan perlindungan berupa biaya pengobatan, perawatan, dan santunan jika PMI mengalami kecelakaan kerja atau penyakit akibat lingkungan kerja, baik sebelum, selama, maupun setelah bekerja.
                                    <br>b.	Jaminan Kematian (JKM): Memberikan santunan kepada ahli waris PMI jika terjadi kematian selama masa perlindungan.
                                    <br>c.	Jaminan Hari Tua (JHT): Tabungan yang dapat dicairkan saat PMI memasuki usia pensiun, mengalami cacat total tetap, atau meninggal dunia.
                                    <br>d.	Asuransi Tambahan: Untuk risiko tertentu yang tidak tercakup dalam program BPJS, PMI juga dapat memperoleh perlindungan tambahan melalui asuransi swasta, misalnya asuransi kesehatan dan asuransi kecelakaan kerja yang diwajibkan oleh negara penempatan.
                                    <br><br>Dasar hukum, Hak atas jaminan sosial dan asuransi bagi PMI diatur dalam beberapa regulasi, antara lain:
                                    <br>a.	Undang-Undang Nomor 18 Tahun 2017 tentang Perlindungan Pekerja Migran Indonesia
                                    <br>b.	Undang-Undang Nomor 40 Tahun 2004 tentang Sistem Jaminan Sosial Nasional
                                    <br>c.	Peraturan Menteri Ketenagakerjaan Nomor 4 Tahun 2023 tentang Jaminan Sosial Pekerja Migran Indonesia
                                    <br>d.	Peraturan Menteri Ketenagakerjaan Nomor 18 Tahun 2018
                                    <br><br>Selain jaminan dari Indonesia, di beberapa negara penempatan seperti Singapura, majikan juga diwajibkan menyediakan asuransi kesehatan dan kecelakaan kerja untuk pekerja migran. Jika majikan tidak memenuhi kewajiban ini, mereka dapat dikenai sanksi sesuai peraturan negara setempat

